    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        String cpfV = cpf.getText();
        String usuarioV = usuario.getText();
        char[] senhaNovaChar = senhaNova.getPassword();
        String senhaNovaV = new String(senhaNovaChar);
        char[] senhaRepetirChar = repetirSenha.getPassword();
        String senhaRepetirV = new String(senhaRepetirChar);

        if (!senhaNovaV.equals(senhaRepetirV)) {
            avisos.setText(avisos.getText() + " - As Senhas não conferem\n");

        }
        if ((senhaNovaV.length() < 5) || (senhaNovaV.length() > 8)) {
            avisos.setText(avisos.getText() + "- Utilize no mínimo 5 e no máximo 8 caracteres para a senha\n");
        }

        Cliente cliente = null;
        ClienteDao clienteDao = new ClienteDaoImpl(ConexaoBanco.getConnection());
        cliente = clienteDao.buscarTudoCliente(usuarioV);
        try {
            ConexaoBanco.getConnection().commit();
        } catch (SQLException ex) {
            Logger.getLogger(AlterarSenhaDoClienteLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        cliente.setUsuario(usuarioV);

        boolean updateCorreto = false;
        if (cliente == null) {
            avisos.setText("Seu nome de usuario está incorreto!");
        } else {
            if (senhaNovaV.equals(senhaRepetirV)) {
                if (cliente.getCpf().equals(cpfV)) {
                    cliente.setSenha(senhaNovaV);
                    updateCorreto = clienteDao.alterarDados(cliente);
                    try {
                        ConexaoBanco.getConnection().commit();
                    } catch (SQLException ex) {
                        Logger.getLogger(AlterarSenhaDoClienteLogin.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }

        if (updateCorreto) {
            JOptionPane.showMessageDialog(this, "Alteração realizada com sucesso!");
            dispose();
        }


    }//GEN-LAST:event_confirmarActionPerformed

